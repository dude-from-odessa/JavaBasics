package com.syntax.class02;

public class WaysToCreateVariable {
	
	public static void main(String[] args) {
		
		int year = 2022; // create a varible and store value
		
		System.out.println(year);
		
		year = 2023; // reassigning the value of same box ( to reuse the variable)
		
		System.out.println(year); // order is important
		
		/*
		 * 1 way to create a variable or declare
		 * dataType name = value
		 */
		
		//declare a variable and we initialized it
		int temperature=60; 
		
		// to create variable we need name and data type
		
		/*
		 * 2 way that is done in 2 steps
		 * create a variable (dataType and name)
		 * assign the value
		 */
		
		int sum;  // created a variable or declare
		sum=30; // assign the value
		sum=50; // reassign the value
		
		int num1, num2, num3; //create 3 variables of int type
		num1=10; // assign the value
		num2=20; // assign the value
		num3=30; // assign the value
		
		System.out.println(sum);
		
		
		
		
		
		
		
	}

}
