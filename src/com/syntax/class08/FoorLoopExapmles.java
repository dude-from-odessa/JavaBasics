package com.syntax.class08;

public class FoorLoopExapmles {

	public static void main(String[] args) {

		// I need to print numbers from 1 to 90

		for (int i = 1; i <= 90; i++) {
			System.out.print(i + " ");
		}

		/*
		 * 1.start point 2.end point 3.increment/decrement
		 */
		
		System.out.println("---------------------");
		
		// I need numbers from 60 to 10

		for (int i = 60; i >= 10; i--) {
			System.out.print(i+" ");
		
		}	
		
	}

}
